vim.keymap.set('n', '<leader>q', vim.diagnostic.setloclist)
-- LSP settings.
--  This function gets run when an LSP connects to a particular buffer.
local on_attach = function(_, bufnr)
  -- NOTE: Remember that lua is a real programming language, and as such it is possible
  -- to define small helper and utility functions so you don't have to repeat yourself
  -- many times.
  --
  -- In this case, we create a function that lets us more easily define mappings specific
  -- for LSP related items. It sets the mode, buffer and description for us each time.
  require("luasnip.loaders.from_vscode").lazy_load { paths = { vim.fn.stdpath("config") .. "/snippets" } }
  local opts = {buffer = bufnr, remap = false}
  vim.keymap.set("n", "gd", vim.lsp.buf.definition, opts)
  vim.keymap.set("n", "K", vim.lsp.buf.hover, opts)
  vim.keymap.set("n", "[d", vim.diagnostic.goto_next, opts)
  vim.keymap.set("n", "]d", vim.diagnostic.goto_prev, opts)
  vim.keymap.set("i", "<C-h>", vim.lsp.buf.signature_help, opts)
  local wk = require("which-key")
wk.register({
  ["<leader>v"] = { name = "+lsp" },
  ["<leader>vws"] = { vim.lsp.buf.workspace_symbol, "workspace_symbol" },
  ["<leader>vrn"] = { vim.lsp.buf.rename, "rename" },
  ["<leader>vrr"] = { vim.lsp.buf.references, "references" },
  ["<leader>vd"] = {vim.diagnostic.open_float, "diagnostic.open_float" },
  ["<leader>vca"] = {vim.lsp.buf.code_action, "code_action" }
} ,{mode = "n"})

  -- Create a command `:Format` local to the LSP buffer
  vim.api.nvim_buf_create_user_command(bufnr, 'Format', function(_)
    if vim.lsp.buf.format then
      vim.lsp.buf.format()
    elseif vim.lsp.buf.formatting then
      vim.lsp.buf.formatting()
    end
  end, { desc = 'Format current buffer with LSP' })
end

